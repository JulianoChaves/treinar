package br.com.treinar.estudo;

public class ComandoCondicionalIf {

	public static void main(String[] args) {

		int a = 10;
		boolean b = true;

		if (a == 10 && b) {
			System.out.println("a == 10 && b: verdadeiro");
		} else {
			System.out.println("a == 10 && b: falso");
		}

	}

}
